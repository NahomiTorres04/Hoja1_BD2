    private void activar_desactivar(boolean valor) {
        txtcarne.setEnabled(valor);
        txtapellido.setEnabled(valor);
        txtnombre1.setEnabled(valor);
        txtedad.setEnabled(valor);
        Insertar.setEnabled(valor);
        Cancelar.setEnabled(valor);
    }

    private void cancelar() {
        int n = JOptionPane.showConfirmDialog(null, "Â¿Realmente desea cancelar?", "CANCELAR", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            limpiarCampos();
            data.Commit_Rollback(false);
            nuevo.setEnabled(true);
            guardar.setEnabled(false);
            Insertar.setEnabled(false);
            Cancelar.setEnabled(false);
        }
    }

    private void limpiarCampos() {
        txtcarne.setText("");
        txtapellido.setText("");
        txtnombre1.setText("");
        txtedad.setText("");
    }

    private boolean verificarCampos() {
        if (txtnombre1.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo Nombres aÃºn vacÃ­o");
            txtnombre1.requestFocus();
            return false;
        } else if (txtapellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo Apellidos aÃºn vacÃ­o");
            txtapellido.requestFocus();
            return false;
        } else if (txtedad.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo edad aÃºn vacÃ­o");
            txtedad.requestFocus();
            return false;
        } else if (txtcarne.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo carnÃ© aÃºn vacÃ­o");
            txtcarne.requestFocus();
            return false;
        } else {
            return true;
        }
    }
